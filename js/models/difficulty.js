class Difficulty {
    constructor() {
        this.arr = [1, 2, 3];
        this.diff = this.arr[0];
    }

    increaseDiff() {
        if (this.diff = this.arr[2]) {
            return;
        } else {
            this.diff++;
        }
    }

    decreaseDiff() {
        if(this.diff = this.arr[0]) {
            return;
        } else {
            this.diff--;
        }
    }

    getDiff() {
        return this.diff;
    }
    
}